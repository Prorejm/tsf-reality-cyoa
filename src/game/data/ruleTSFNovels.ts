/**
 * 根据规则编号获取对应的TSF小说数据
 */
export function getNovelByRuleId(ruleId: number): RuleTSFNovel | undefined {
  return RULE_TSF_NOVELS.find((novel) => novel.ruleId === ruleId);
}

/**
 * 根据游戏状态检查已违反规则的TSF描写列表
 * 逻辑与 SafetyRulesBook 中的 violatedRules 计算保持一致
 */
export function checkViolatedNovels(state: {
  flags?: Record<string, any>;
  erosionLevel?: number;
}): RuleTSFNovel[] {
  const completedNodes: string[] = state.flags?.completed_nodes ?? [];
  const erosionLevel = state.erosionLevel ?? 0;
  const violated = new Set<number>();

  for (const rule of SAFETY_RULES) {
    if (completedNodes.includes(rule.nodeId)) {
      violated.add(rule.id);
      continue;
    }
    if (rule.effects.setFlag && state.flags?.[rule.effects.setFlag]) {
      violated.add(rule.id);
      continue;
    }
    if (rule.effects.erosion && erosionLevel >= rule.effects.erosion) {
      violated.add(rule.id);
    }
  }

  return RULE_TSF_NOVELS.filter((novel) => violated.has(novel.ruleId));
}
