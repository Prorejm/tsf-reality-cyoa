/**
 * 根据 ID 查找结局
 */
export function getEndingById(id: string): EndingEntry | undefined {
  return ENDINGS.find((e) => e.id === id);
}

/**
 * 检查状态是否满足某个结局的条件
 */
function meetsCondition(state: any, cond: EndingCondition): boolean {
  // 侵蚀度范围
  if (cond.minErosion !== undefined && (state.erosionLevel ?? 0) < cond.minErosion) return false;
  if (cond.maxErosion !== undefined && (state.erosionLevel ?? 0) > cond.maxErosion) return false;

  // 认知度范围
  if (cond.minAwareness !== undefined && (state.awarenessLevel ?? 0) < cond.minAwareness) return false;
  if (cond.maxAwareness !== undefined && (state.awarenessLevel ?? 0) > cond.maxAwareness) return false;

  // 天数范围
  if (cond.minDay !== undefined && (state.currentDay ?? 1) < cond.minDay) return false;
  if (cond.maxDay !== undefined && (state.currentDay ?? 1) > cond.maxDay) return false;

  // 必需标志
  if (cond.requiredFlags) {
    for (const [key, value] of Object.entries(cond.requiredFlags)) {
      const flagVal = state.flags?.[key];
      if (typeof value === 'boolean') {
        if (!!flagVal !== value) return false;
      } else if (typeof value === 'number') {
        if (typeof flagVal !== 'number' || flagVal < value) return false;
      } else if (typeof value === 'string') {
        if (String(flagVal) !== value) return false;
      } else {
        if (flagVal !== value) return false;
      }
    }
  }

  // 必需物种
  if (cond.requiredSpecies && state.flags?.player_species !== cond.requiredSpecies) return false;
  if (cond.requiredGender && state.flags?.player_gender !== cond.requiredGender) return false;
  if (cond.requiredAge && state.flags?.player_age !== cond.requiredAge) return false;

  // 必需道具
  if (cond.requiredItems) {
    const inventory = state.inventory ?? [];
    for (const itemId of cond.requiredItems) {
      if (!inventory.some((i: any) => i.id === itemId)) return false;
    }
  }

  // 已完成节点
  if (cond.completedNodes) {
    const nodes = state.flags?.completed_nodes ?? state.flags?.cyoaCompletedNodes ?? [];
    for (const nodeId of cond.completedNodes) {
      if (!nodes.includes(nodeId)) return false;
    }
  }

  if (cond.minCompletedNodes !== undefined) {
    const nodes = state.flags?.completed_nodes ?? state.flags?.cyoaCompletedNodes ?? [];
    if (nodes.length < cond.minCompletedNodes) return false;
  }

  return true;
}

/**
 * 检查状态匹配所有满足条件的结局
 */
export function checkEndingConditions(state: any, endings: EndingEntry[] = ENDINGS): EndingEntry[] {
  return endings.filter((ending) => meetsCondition(state, ending.conditions));
}
